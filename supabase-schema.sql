create table if not exists public.companies (
  id uuid primary key default gen_random_uuid(),
  name text not null,
  slug text unique not null,
  tagline text not null default '',
  description text not null default '',
  contact_email text not null default '',
  logo_url text not null default '',
  brand_primary_color text not null default '#2563eb',
  brand_secondary_color text not null default '#7c3aed',
  onboarding_step int not null default 1,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

create table if not exists public.users (
  id uuid primary key default gen_random_uuid(),
  email text unique not null,
  password_hash text not null,
  display_name text not null default '',
  role text not null default 'user' check (role in ('admin', 'user')),
  company_id uuid references public.companies(id) on delete set null,
  onboarding_complete boolean not null default false,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

create index if not exists users_email_idx on public.users (email);
create index if not exists users_company_id_idx on public.users (company_id);
create index if not exists companies_slug_idx on public.companies (slug);

-- Prevent a second admin from being inserted within the same company.
-- Each company can have one admin row, but different companies may each have their own admin.
drop index if exists public.users_single_admin_idx;
create unique index if not exists users_single_admin_idx on public.users (company_id) where role = 'admin';

