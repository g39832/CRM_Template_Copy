-- =============================================
-- Business workflow preference (Step 2)
-- =============================================
-- Add business_workflow column if it doesn't already exist.
-- Default is 'both' so existing companies continue to work.
DO $$
BEGIN
  IF NOT EXISTS (
    SELECT 1 FROM information_schema.columns
    WHERE table_schema = 'public' AND table_name = 'companies' AND column_name = 'business_workflow'
  ) THEN
    ALTER TABLE public.companies ADD COLUMN business_workflow text NOT NULL DEFAULT 'both'
      CHECK (business_workflow IN ('single', 'returning', 'both'));
  END IF;
END $$;

-- Force Supabase schema cache refresh so the new column is immediately visible.
-- Without this, the PostgREST API will return:
--   "Could not find the 'business_workflow' column"
-- This NOTIFY command invalidates the schema cache across all connections.
NOTIFY pgrst, 'reload schema';

