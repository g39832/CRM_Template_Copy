-- =============================================
-- Admin claimed guard (registration enforcement)
-- =============================================
-- Track whether the first admin has registered.  Uses the existing
-- `settings` table (key/value store) so no new table is needed.
-- The server queries this flag to decide whether to redirect visitors
-- to /register or show the normal login page.
--
-- Seed row (insert if missing):
INSERT INTO public.settings (key, value)
SELECT 'admin_claimed', 'false'
WHERE NOT EXISTS (SELECT 1 FROM public.settings WHERE key = 'admin_claimed');

