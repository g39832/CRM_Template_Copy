-- =============================================
-- Client type tagging (Feature 6)
-- =============================================
-- Add client_type column if it doesn't already exist.
-- Uses DEFAULT 'one-off' so existing rows are backfilled automatically.
DO $$
BEGIN
  IF NOT EXISTS (
    SELECT 1 FROM information_schema.columns
    WHERE table_schema = 'public' AND table_name = 'clients' AND column_name = 'client_type'
  ) THEN
    ALTER TABLE public.clients ADD COLUMN client_type text NOT NULL DEFAULT 'one-off'
      CHECK (client_type IN ('one-off', 'recurring'));
  END IF;
END $$;

-- Force schema cache refresh so the new column is immediately visible.
-- PostgREST caches the schema; without this, you may see
-- "Could not find the 'client_type' column" errors.
NOTIFY pgrst, 'reload schema';

-- Verification: query the column to confirm it exists in the schema cache
SELECT column_name, data_type, is_nullable, column_default
FROM information_schema.columns
WHERE table_schema = 'public' AND table_name = 'clients' AND column_name = 'client_type';

