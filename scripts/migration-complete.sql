-- 9a. COMPANIES (with business_workflow column)
CREATE TABLE IF NOT EXISTS public.companies (
  id                    UUID PRIMARY KEY DEFAULT gen_random_uuid(),
  name                  TEXT NOT NULL,
  slug                  TEXT UNIQUE NOT NULL,
  tagline               TEXT NOT NULL DEFAULT '',
  description           TEXT NOT NULL DEFAULT '',
  contact_email         TEXT NOT NULL DEFAULT '',
  logo_url              TEXT NOT NULL DEFAULT '',
  brand_primary_color   TEXT NOT NULL DEFAULT '#2563eb',
  brand_secondary_color TEXT NOT NULL DEFAULT '#7c3aed',
  onboarding_step       INT NOT NULL DEFAULT 1,
  created_at            TIMESTAMPTZ NOT NULL DEFAULT now(),
  updated_at            TIMESTAMPTZ NOT NULL DEFAULT now()
);

-- Add business_workflow column (idempotent)
DO $$
BEGIN
  IF NOT EXISTS (
    SELECT 1 FROM information_schema.columns
    WHERE table_schema = 'public' AND table_name = 'companies' AND column_name = 'business_workflow'
  ) THEN
    ALTER TABLE public.companies ADD COLUMN business_workflow TEXT NOT NULL DEFAULT 'both'
      CHECK (business_workflow IN ('single', 'returning', 'both'));
  END IF;
END $$;

-- Add currency column (idempotent — for multi-currency feature)
DO $$
BEGIN
  IF NOT EXISTS (
    SELECT 1 FROM information_schema.columns
    WHERE table_schema = 'public' AND table_name = 'companies' AND column_name = 'currency'
  ) THEN
    ALTER TABLE public.companies ADD COLUMN currency TEXT NOT NULL DEFAULT 'USD';
  END IF;
END $$;

CREATE INDEX IF NOT EXISTS companies_slug_idx ON public.companies (slug);

-- 9b. USERS
CREATE TABLE IF NOT EXISTS public.users (
  id                  UUID PRIMARY KEY DEFAULT gen_random_uuid(),
  email               TEXT UNIQUE NOT NULL,
  password_hash       TEXT NOT NULL,
  display_name        TEXT NOT NULL DEFAULT '',
  role                TEXT NOT NULL DEFAULT 'user' CHECK (role IN ('admin', 'user')),
  company_id          UUID REFERENCES public.companies(id) ON DELETE SET NULL,
  onboarding_complete BOOLEAN NOT NULL DEFAULT FALSE,
  created_at          TIMESTAMPTZ NOT NULL DEFAULT now(),
  updated_at          TIMESTAMPTZ NOT NULL DEFAULT now()
);

CREATE INDEX IF NOT EXISTS users_email_idx      ON public.users (email);
CREATE INDEX IF NOT EXISTS users_company_id_idx  ON public.users (company_id);

-- Prevent a second admin (unique partial index)
CREATE UNIQUE INDEX IF NOT EXISTS users_single_admin_idx
  ON public.users ((TRUE)) WHERE role = 'admin';

-- 9c. COMPANY COMPONENTS (Step 3 feature selection)
CREATE TABLE IF NOT EXISTS public.company_components (
  id              BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  company_id      UUID NOT NULL REFERENCES public.companies(id) ON DELETE CASCADE,
  component_type  TEXT NOT NULL,
  is_active       BOOLEAN NOT NULL DEFAULT TRUE,
  display_order   INT NOT NULL DEFAULT 0,
  config          JSONB NOT NULL DEFAULT '{}',
  created_at      TIMESTAMPTZ NOT NULL DEFAULT now(),
  updated_at      TIMESTAMPTZ NOT NULL DEFAULT now()
);

CREATE INDEX IF NOT EXISTS company_components_company_id_idx      ON public.company_components (company_id);
CREATE INDEX IF NOT EXISTS company_components_company_order_idx   ON public.company_components (company_id, display_order);
CREATE UNIQUE INDEX IF NOT EXISTS company_components_unique_type_per_company
  ON public.company_components (company_id, component_type);

-- 9d. ACTIVITY LOG
CREATE TABLE IF NOT EXISTS public.activity_log (
  id          BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  company_id  UUID NOT NULL REFERENCES public.companies(id) ON DELETE CASCADE,
  user_id     UUID REFERENCES public.users(id) ON DELETE SET NULL,
  action      TEXT NOT NULL,
  entity_type TEXT,
  entity_id   TEXT,
  details     JSONB,
  created_at  TIMESTAMPTZ NOT NULL DEFAULT now()
);

CREATE INDEX IF NOT EXISTS activity_log_company_id_idx  ON public.activity_log (company_id);
CREATE INDEX IF NOT EXISTS activity_log_created_at_idx  ON public.activity_log (created_at DESC);
CREATE INDEX IF NOT EXISTS activity_log_action_idx      ON public.activity_log (action);
CREATE INDEX IF NOT EXISTS activity_log_entity_type_idx ON public.activity_log (entity_type);

-- 9e. EMAIL TEMPLATES
CREATE TABLE IF NOT EXISTS public.email_templates (
  id          BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  company_id  UUID NOT NULL REFERENCES public.companies(id) ON DELETE CASCADE,
  name        TEXT NOT NULL,
  subject     TEXT NOT NULL,
  body        TEXT NOT NULL,
  type        TEXT NOT NULL DEFAULT 'custom',
  variables   JSONB DEFAULT '{}',
  created_at  TIMESTAMPTZ NOT NULL DEFAULT now(),
  updated_at  TIMESTAMPTZ NOT NULL DEFAULT now()
);

CREATE INDEX IF NOT EXISTS email_templates_company_id_idx ON public.email_templates (company_id);

