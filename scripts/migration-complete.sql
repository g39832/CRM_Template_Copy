  recurring       BOOLEAN DEFAULT FALSE,
  expense_date    TIMESTAMPTZ NOT NULL DEFAULT now(),
  notes           TEXT DEFAULT '',
  attachment_url  TEXT DEFAULT '',
  created_at      TIMESTAMPTZ NOT NULL DEFAULT now(),
  updated_at      TIMESTAMPTZ NOT NULL DEFAULT now()
);

