-- ===============================================================
-- 1. SETTINGS TABLE (key/value store)
-- ===============================================================
CREATE TABLE IF NOT EXISTS public.settings (
  key   TEXT PRIMARY KEY,
  value TEXT NOT NULL
);

-- Seed admin_claimed row if missing
INSERT INTO public.settings (key, value)
SELECT 'admin_claimed', 'false'
WHERE NOT EXISTS (SELECT 1 FROM public.settings WHERE key = 'admin_claimed');

-- ===============================================================
-- 2. CLIENTS TABLE (with client_type column)
-- ===============================================================
CREATE TABLE IF NOT EXISTS public.clients (
  id           BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  name         TEXT NOT NULL,
  phone        TEXT DEFAULT '',
  email        TEXT DEFAULT '',
  address      TEXT DEFAULT '',
  status       TEXT DEFAULT 'Lead',
  total_due    NUMERIC DEFAULT 0,
  amount_paid  NUMERIC DEFAULT 0,
  balance      NUMERIC DEFAULT 0,
  scope_of_work TEXT DEFAULT '',
  job_cost     NUMERIC DEFAULT 0,
  created_at   TIMESTAMPTZ NOT NULL DEFAULT now()
);

-- Add client_type column (idempotent)
DO $$
BEGIN
  IF NOT EXISTS (
    SELECT 1 FROM information_schema.columns
    WHERE table_schema = 'public' AND table_name = 'clients' AND column_name = 'client_type'
  ) THEN
    ALTER TABLE public.clients ADD COLUMN client_type TEXT NOT NULL DEFAULT 'one-off'
      CHECK (client_type IN ('one-off', 'recurring'));
  END IF;
END $$;

-- ===============================================================
-- 3. PAYMENTS TABLE
-- ===============================================================
CREATE TABLE IF NOT EXISTS public.payments (
  id           BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  client_id    BIGINT REFERENCES public.clients(id) ON DELETE CASCADE,
  amount       NUMERIC NOT NULL DEFAULT 0,
  payment_date TIMESTAMPTZ NOT NULL DEFAULT now()
);

-- ===============================================================
-- 4. NOTES TABLE
-- ===============================================================
CREATE TABLE IF NOT EXISTS public.notes (
  id         BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  client_id  BIGINT REFERENCES public.clients(id) ON DELETE CASCADE,
  content    TEXT NOT NULL,
  created_at TIMESTAMPTZ NOT NULL DEFAULT now()
);

-- ===============================================================
-- 5. FINANCE OVERRIDES TABLE
-- ===============================================================
CREATE TABLE IF NOT EXISTS public.finance_overrides (
  year            INT PRIMARY KEY,
  total_expected  NUMERIC DEFAULT 0,
  total_received  NUMERIC DEFAULT 0,
  total_remaining NUMERIC DEFAULT 0,
  total_clients   INT DEFAULT 0,
  notes           TEXT,
  updated_at      TIMESTAMPTZ NOT NULL DEFAULT now()
);

-- ===============================================================
-- 6. JOBS TABLE
-- ===============================================================
CREATE TABLE IF NOT EXISTS public.jobs (
  id           BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  client_id    BIGINT REFERENCES public.clients(id) ON DELETE CASCADE,
  title        TEXT DEFAULT 'New Job',
  status       TEXT DEFAULT 'Prospect',
  scope_of_work TEXT DEFAULT '',
  total_due    NUMERIC DEFAULT 0,
  amount_paid  NUMERIC DEFAULT 0,
  balance      NUMERIC DEFAULT 0,
  job_cost     NUMERIC DEFAULT 0,
  created_at   TIMESTAMPTZ NOT NULL DEFAULT now()
);

CREATE INDEX IF NOT EXISTS jobs_client_id_idx    ON public.jobs (client_id);
CREATE INDEX IF NOT EXISTS jobs_created_at_idx   ON public.jobs (created_at DESC);

-- ===============================================================
-- 7. FINANCE MARGIN ENTRIES TABLE
-- ===============================================================
CREATE TABLE IF NOT EXISTS public.finance_margin_entries (
  id              BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
